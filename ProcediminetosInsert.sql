CREATE PROCEDURE INSERTAR_PEDIDO(IN RUT_I INT,
IN PRODUCTOS_I VARCHAR(100),IN CANT_PRO_I INT, IN TOTAL_I INT, IN NRO_MESA_I INT, IN ESTADO_I INT)
BEGIN
	INSERT INTO PEDIDO (RUT,PRODUCTOS,CANT_PRO,TOTAL,NRO_MESA,ESTADO) 
    VALUES (RUT_I,PRODUCTOS_I,CANT_PRO_I,TOTAL_I,NRO_MESA_I,ESTADO_I);
END$


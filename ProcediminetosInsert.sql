DELIMITER $
CREATE PROCEDURE INSERTAR_DETALLE_PEDIDO(IN ID_PEDIDO_I INT,
IN ID_PRODUCTO_I INT,IN RUT_I INT,IN CANTIDAD_I INT)
BEGIN
	DECLARE CANT_I INT;
    DECLARE PRECIO_I INT;
	SET CANT_I = (SELECT CONTAR_PRO(ID_PEDIDO_I));
    SET PRECIO_I = (SELECT PRECIO FROM PRODUCTO WHERE ID_PRODUCTO = ID_PRODUCTO_I);
    CALL ACTUALIZAR_CANT_PEDIDO(ID_PEDIDO_I, CANT_I);
	INSERT INTO DETALLE_PEDIDO (ID_PEDIDO,ID_PRODUCTO,RUT,CANTIDAD,PRECIO) VALUES (ID_PEDIDO_I,ID_PRODUCTO_I,RUT_I,CANTIDAD_I,PRECIO_I);
END$

